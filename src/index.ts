// string
let myString: string = "Hello, TypeScript!";
console.log(myString);  // Hello, TypeScript!

// number
let myNumber: number = 42;
console.log(myNumber);  // 42

// boolean
let myBoolean: boolean = true;
console.log(myBoolean); // true

// null 
let myNull: null = null;
console.log(myNull);    // null

// any 
let myAny: any = "I can be anything!";
console.log(myAny); // I can be anything!

myAny = "123";
console.log(myAny); // 123

myAny = false;
console.log(myAny); // false

// any 배열
let anyList: any[] = [123,"가나다",true];

console.log(anyList);   // [ 123, '가나다', true ]


